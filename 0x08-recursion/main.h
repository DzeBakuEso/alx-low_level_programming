#ifndef MAIN_H
#define MAIN_H

#include <stdio.h>

/* Prototype for the function that prints a string using recursion */
void _puts_recursion(char *s);

/* Prototype for the function that prints a string in reverse using recursion */
void _print_rev_recursion(char *s);

/* Prototype for the function that returns the length of a string using recursion */
int _strlen_recursion(char *s);

/* Prototype for the function that returns the factorial of a given number using recursion */
int factorial(int n);

/* Prototype for the function that returns the value of x raised to the power of y using recursion */
int _pow_recursion(int x, int y);

/* Prototype for the function that returns the natural square root of a number using recursion */
int _sqrt_recursion(int n);

/* Prototype for the function that returns 1 if the input integer is a prime number, otherwise returns 0 */
int is_prime_number(int n);

/* Prototype for the _putchar function */
int _putchar(char c);

#endif /* MAIN_H */

